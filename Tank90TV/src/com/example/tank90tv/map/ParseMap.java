	private Canvas mBitmapCanvas = null;
	private Bitmap backgroundBMP = null;
	private Context context;
	private int[]screenSize;
	
	public ParseMap(Context context){
		this.context = context;
		this.screenSize = MyApplication.getScreenSize();
		
		backgroundBMP = Bitmap.createBitmap(screenSize[0],screenSize[1], Bitmap.Config.ARGB_8888);
        mBitmapCanvas = new Canvas(backgroundBMP);
        mBitmapCanvas.drawColor(Color.BLACK);
	}
	public void initMap(){
		 new Thread(new Runnable() {
				@Override
				public void run() {
					// TODO Auto-generated method stub
					for(int x=0;x<MAP[0].length;x++){
						for(int y=0;y<MAP.length;y++){
							drawMap(x,y);
						}
					}
				}
			}).start();
	}
	
	public void drawMap(int x,int y){
		int bmWidth = screenSize[0]/MAP[0].length;
		int bmHeight = screenSize[1]/MAP.length;
		Bitmap bitmap = BitmapFactory.decodeResource(context.getResources(), initMapBMP(MAP[x][y]));
		Rect rect = new Rect(0, 0, bitmap.getWidth(), bitmap.getHeight());
		RectF rectF = new RectF(x*bmWidth, y*bmHeight, x*bmWidth+bmWidth, y*bmHeight+bmHeight);
		 
		mBitmapCanvas.drawBitmap(bitmap, rect, rectF, null);
	}
	
